package ex1;

public class Calcul {

    /**
     * @param a
     * @param b
     * @return addition des deux nombres a et b
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * @param a
     * @param b
     * @return soustraction des deux nombres a et b
     */
    public int soustraction(int a, int b) {
        return a - b;
    }
}
